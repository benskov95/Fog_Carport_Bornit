package FunctionLayer;

public class Size {

    private int sizeId;
    private int size;

    public Size(int sizeId, int size) {
        this.sizeId = sizeId;
        this.size = size;
    }

    public int getSizeId() {
        return sizeId;
    }

    public void setSizeId(int sizeId) {
        this.sizeId = sizeId;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
